## Static Array, Dynamic Array, Strings

A = [10, 20, 30]
print(A)

# Inserting into the Array - Append - O(1)
A.append(50)
print(A)

# Deleting an element from the last position - O(1)
A.pop()
print(A)

# Insert at a certain position - O(n)
A.insert(2, 100)
print(A)

## changing certain index value
A[1] = 5
print(A)


